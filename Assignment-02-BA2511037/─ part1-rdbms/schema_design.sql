-- -----------------------------------------------------------------------------
-- TABLE: orders
-- Each row is one line item (one product on one order).  Foreign keys reference
-- the three dimension tables above.  quantity and order_date are genuine facts
-- about the order itself and are not derivable from any single FK column, so
-- this table is in 3NF.
--
-- order_id + product_id forms the composite primary key because the source data
-- has one row per (order, product) combination.
-- -----------------------------------------------------------------------------
CREATE TABLE orders (
    order_id     CHAR(7)      NOT NULL,
    customer_id  CHAR(4)      NOT NULL,
    product_id   CHAR(4)      NOT NULL,
    sales_rep_id CHAR(4)      NOT NULL,
    quantity     SMALLINT     NOT NULL CHECK (quantity > 0),
    order_date   DATE         NOT NULL,
    CONSTRAINT pk_orders      PRIMARY KEY (order_id, product_id),
    CONSTRAINT fk_ord_customer  FOREIGN KEY (customer_id)  REFERENCES customers (customer_id),
    CONSTRAINT fk_ord_product   FOREIGN KEY (product_id)   REFERENCES products  (product_id),
    CONSTRAINT fk_ord_sales_rep FOREIGN KEY (sales_rep_id) REFERENCES sales_reps(sales_rep_id)
);

