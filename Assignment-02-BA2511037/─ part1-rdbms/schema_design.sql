-- -----------------------------------------------------------------------------
-- TABLE: sales_reps
-- Stores one row per sales representative.  Eliminates the update anomaly where
-- SR01's office_address was stored in two different forms across 83 order rows
-- ("Nariman Point" vs "Nariman Pt").  Now office_address is stored once and a
-- single UPDATE keeps every order reference consistent.
-- -----------------------------------------------------------------------------
CREATE TABLE sales_reps (
    sales_rep_id   CHAR(4)      NOT NULL,
    sales_rep_name VARCHAR(100) NOT NULL,
    email          VARCHAR(150) NOT NULL,
    office_address VARCHAR(255) NOT NULL,
    CONSTRAINT pk_sales_reps PRIMARY KEY (sales_rep_id)
);

